package com.bon.apetitum.demo;

public class FoodItem {
    public int id;
    public String name;
    public String desc;
    public int price;

    public FoodItem(int id, String name, String desc, int price) {
        this.id = id;
        this.name = name;
        this.desc = desc;
        this.price = price;
    }
}